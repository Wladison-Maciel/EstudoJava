package variaveis;

public class TiposVariaveis {
    public static void main(String [] args){
        int idade = 17; // Tipo Inteiro
        double salario = 1502.0; // Tipo Double (Armazena mais números decimais 10 casas)
        float pontuacao = 11.24f; // Tipo Float (Armazena menos números decimais 6 casas)
        String nome = "Wladison"; // Tipo String (Armazena palavras)
        char letra = 'W'; // Tipo Char (Parecido com String mas armazena somente uma letra)
        boolean escolha = true; // Tipo Booleano (Serve para escolhas True ou False)

        final int maiorIdade = 18; // Tipo Int mas Const (Variável que não pode ter seu valor alterado)
    }
}